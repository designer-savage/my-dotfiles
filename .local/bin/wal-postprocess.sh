#!/bin/bash
# Post-wal theming: regenerates colors for waybar, rofi, swaync, gtk

COLORS_JSON="$HOME/.cache/wal/colors.json"

if [ ! -f "$COLORS_JSON" ]; then
    echo "wal-postprocess: colors.json not found" >&2
    exit 1
fi

# ─── Waybar colors.css ─────────────────────────────────────────────────────
python3 - "$COLORS_JSON" > "$HOME/.config/waybar/colors.css" <<'PYEOF'
import json, sys
c = json.load(open(sys.argv[1]))
sp = c['special']
co = c['colors']
print(f"""/* Generated by wal-postprocess.sh — do not edit manually */
@define-color background  {sp['background']};
@define-color foreground  {sp['foreground']};
@define-color color1      {co['color1']};
@define-color color2      {co['color2']};
@define-color color4      {co['color4']};
@define-color color5      {co['color5']};
@define-color color8      {co['color8']};
@define-color color13     {co['color13']};""")
PYEOF

# ─── Rofi colors ───────────────────────────────────────────────────────────
cp "$HOME/.cache/wal/colors-rofi-dark.rasi" "$HOME/.config/rofi/colors.rasi" 2>/dev/null

# ─── swaync style ──────────────────────────────────────────────────────────
if [ -f "$HOME/.cache/wal/colors-swaync.css" ]; then
    cp -f --remove-destination "$HOME/.cache/wal/colors-swaync.css" \
        "$HOME/.config/swaync/style.css" 2>/dev/null || true
    pkill -SIGUSR1 swaync 2>/dev/null || true
fi

# ─── GTK3 ──────────────────────────────────────────────────────────────────
[ -f "$HOME/.cache/wal/gtk3.css" ] && \
    cp "$HOME/.cache/wal/gtk3.css" "$HOME/.config/gtk-3.0/gtk.css" 2>/dev/null

# ─── Waybar hot-reload CSS ─────────────────────────────────────────────────
pkill -SIGUSR2 waybar 2>/dev/null || true
